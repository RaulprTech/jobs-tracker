import { useState,  useRef } from 'react'
import Head from 'next/head'
import styles from '../styles/Home.module.css'

export default function Home() {
  const inputRef = useRef();
  const [shortURL, setShortURL] = useState('');

  const handleSubmit = async (e) => {
    e.preventDefault();
    const url = inputRef.current.value;
    
    await fetch('/api/shortUrl', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({url}),
    })
    .then(res => res.json())
    .then(data => { setShortURL(data.shortUrl) });
  }


  return (
    <div className={styles.container}>
      <Head>
        <title>Shortener</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <h1 className={styles.title}>
          Url Shortener
        </h1>

        <p className={styles.description}>
          Acorta tus URLs aqu√≠
        </p>

        <div className={styles.grid}>
          <form className={styles.card} onSubmit={handleSubmit}>
            <input ref={inputRef} type='text' className={styles.input} placeholder='URL'/>
            <button className={styles.button}>Acortar</button>
            <span className={styles.input}>{shortURL}</span>
          </form>
        </div>
      </main>
    </div>
  )
}
